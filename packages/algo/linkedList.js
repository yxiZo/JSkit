/**
 * @description 单链表 
 */
class LinkedList {
    constructor() {
        this.head = null;
        this.tail = null;
    }

}

/**
 * @description 双向链表
 */
class DoublyLinkedList {
    constructor() {
        this.head = null;
        this.tail = null;
    }
}

/**
 * @description 单向循环链表
 */
class CircularLinkedList {
    constructor() {
        this.head = null;
        this.tail = null;
    }
}